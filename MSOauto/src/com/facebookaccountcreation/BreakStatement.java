package com.facebookaccountcreation;

public class BreakStatement {

	public static void main(String[] args) {
		byte b = 100;
		switch(b)
		{
		case 10: System.out.println("mammu");
		break;
		case 101: System.out.println("40");
		break;
		default: System.out.println("default");
		break;
		}
	}

}
